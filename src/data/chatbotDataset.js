// Function to find best match
export const findBestMatch = (userMessage) => {
  const msg = userMessage.toLowerCase().trim();
  
  // Direct match
  if (chatbotDataset[msg]) {
    return chatbotDataset[msg];
  }
  
  // Partial match - find best keyword match
  let bestMatch = null;
  let maxScore = 0;
  
  Object.keys(chatbotDataset).forEach(key => {
    const keywords = key.split(' ');
    let score = 0;
    
    keywords.forEach(keyword => {
      if (msg.includes(keyword)) {
        score++;
      }
    });
    
    if (score > maxScore) {
      maxScore = score;
      bestMatch = key;
    }
  });
  
  // Return match if score is good enough
  if (maxScore >= 2) {
    return chatbotDataset[bestMatch];
  }
  
  return null; // No good match found
};



